package com.jg2b.seoPojo;

public class SkuPojo {

	private int id;// id
	private String name;// 名称
	private String size;// 型号
	private String unit;// 单位
	private int number;// 数量
	private int status;// 审核状态
	private int creator;// 创建人
	private int creatertime;// 创建时间
	private int updator;// 修改人
	private int updatetime;// 修改时间
	private int deleted;// 删除标志
	private int salse;// 销量
	private int sources;// 数据来源
	private String material;// sku材质

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getSize() {
		return size;
	}

	public void setSize(String size) {
		this.size = size;
	}

	public String getUnit() {
		return unit;
	}

	public void setUnit(String unit) {
		this.unit = unit;
	}

	public int getNumber() {
		return number;
	}

	public void setNumber(int number) {
		this.number = number;
	}

	public int getStatus() {
		return status;
	}

	public void setStatus(int status) {
		this.status = status;
	}

	public int getCreator() {
		return creator;
	}

	public void setCreator(int creator) {
		this.creator = creator;
	}

	public int getCreatertime() {
		return creatertime;
	}

	public void setCreatertime(int creatertime) {
		this.creatertime = creatertime;
	}

	public int getUpdator() {
		return updator;
	}

	public void setUpdator(int updator) {
		this.updator = updator;
	}

	public int getUpdatetime() {
		return updatetime;
	}

	public void setUpdatetime(int updatetime) {
		this.updatetime = updatetime;
	}

	public int getDeleted() {
		return deleted;
	}

	public void setDeleted(int deleted) {
		this.deleted = deleted;
	}

	public int getSalse() {
		return salse;
	}

	public void setSalse(int salse) {
		this.salse = salse;
	}

	public int getSources() {
		return sources;
	}

	public void setSources(int sources) {
		this.sources = sources;
	}

	public String getMaterial() {
		return material;
	}

	public void setMaterial(String material) {
		this.material = material;
	}

}
